    public void iniciaMatriz(){
        L = new int[FrameSomVisualization.getGridMUX()][FrameSomVisualization.getGridMUY()];
        for (int i = 0; i < L.length; i++) {
            for (int j = 0; j < L[0].length; j++) {
                L[i][j] = 0;
            }
        }
    }
