    private void jPanel1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseReleased
        if(L == null){
            iniciaMatriz();
        }
        int x1 = evt.getX();
        int y1 = evt.getY();

        int NUM_HEX_CORNERS = 6;
        int[] mCornersX = new int[NUM_HEX_CORNERS];
        int[] mCornersY = new int[NUM_HEX_CORNERS];
        HexGrid mCellMetrics = new HexGrid(FrameSomVisualization.getValueU2D());

        boolean aux = false;

        for (int i = 0; i < FrameSomVisualization.getGridMUX(); i++) {
            for (int j = 0; j < FrameSomVisualization.getGridMUY(); j++) {
                mCellMetrics.setCellIndex(i, j);
                mCellMetrics.computeCorners(mCornersX, mCornersY);
                Polygon p = new Polygon(mCornersX, mCornersY, NUM_HEX_CORNERS);
                if (p.contains(x1, y1)) {
                    if (PanelMarcadores.getP().contains(p)) {
                        PanelMarcadores.getP().remove(p);
                    } else {
                        PanelMarcadores.addP(p);
                        L[i][j] = 1;
                    }
                }
            }
        }
        repaint();        // TODO add your handling code here:
    }//GEN-LAST:event_jPanel1MouseReleased

